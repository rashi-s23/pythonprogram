#WAP to accept a number from 1 to 7 and display the name of the day, like 1 forSunday, 2 for Monday, etc.
number=int(input("Enter the number from 1 to 7 :"))
if(number==1):
    print("sunday")
elif(number==2):
    print("monday")
elif(number==3):
    print("tuesday")
elif(number==4):
    print("wednesday")
elif(number==5):
    print("thursday")
elif(number==6):
    print("friday")
elif(number==7):
    print("saturday")        

    

